for i, article in enumerate(test_articles, 1):
    result, confidence = predict_fake_news(article)
    print(f"\nArticle {i}:")
    print(f"Text: {article[:80]}...")
    print(f"Prediction: {result} ({confidence:.2f}% confidence)")

