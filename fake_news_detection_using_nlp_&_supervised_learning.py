# STEP 7: REAL-TIME PREDICTION FUNCTION
# =============================================================================

print("\n=== Real-Time Prediction Function ===")

def predict_fake_news(text):
    """Predict if news article is fake or real"""
    processed = preprocess_text(text)
    tfidf_transform = tfidf.transform([processed])
    prediction = ensemble_model.predict(tfidf_transform)[0]
    probability = ensemble_model.predict_proba(tfidf_transform)[0]

    result = "FAKE" if prediction == 1 else "REAL"
    confidence = max(probability) * 100

    return result, confidence

